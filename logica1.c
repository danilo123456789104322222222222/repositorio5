#include <stdio.h>

int main() {
    int a = 5; // valor da varilvel "a"
    int b = 3; // valor da variavel "b"
    int result; // variavel "result" que e o resultado da função

    result = a - b; // função onde e o result da var "a" -  var "b"

    printf("O resultado da subtração é: %d\n", result); // o codigo que fala ao usuario que o resultado é a subtração de var "a" - var "b"

    return 0; // indica bem sucedido 
}